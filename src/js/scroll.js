let number01 = document.querySelector('.number01'),
    numberTop01 = number01.getBoundingClientRect().top,
    start01 = +number01.innerHTML,
    end01 = +number01.dataset.max;

window.addEventListener('scroll', function onScroll() {
    if(window.pageYOffset > numberTop01 - window.innerHeight / 1.6) {
        this.removeEventListener('scroll', onScroll);
        let interval01 = setInterval(function() {
            number01.innerHTML = ++start01;
            if(start01 == end01) {
                clearInterval(interval01);
            }
        }, 70);
    }
});

